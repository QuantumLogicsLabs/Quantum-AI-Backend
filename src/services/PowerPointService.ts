import { z } from 'zod';
import { getAiProvider } from '../providers/ai/index.js';
import { documentStorageService } from './DocumentStorageService.js';
import { truncateText } from '../utils/fileTypes.js';
import { ValidationError } from '../utils/errors.js';

const slideSchema = z.object({
  type: z.enum(['title', 'objectives', 'content', 'example', 'diagram', 'summary', 'slo_questions']),
  title: z.string(),
  bullets: z.array(z.string()).optional(),
  notes: z.string().optional(),
});

const presentationSchema = z.object({
  presentationTitle: z.string(),
  subtitle: z.string().optional(),
  slides: z.array(slideSchema),
});

export type PresentationPlan = z.infer<typeof presentationSchema>;

export class PowerPointService {
  async generatePlanFromDocument(
    documentId: string,
    userId: string,
    options?: { subject?: string; gradeLevel?: string; sloTopics?: string[] }
  ): Promise<PresentationPlan> {
    const doc = await documentStorageService.getById(documentId, userId);
    const text = await documentStorageService.getExtractedText(documentId, userId);
    const context = truncateText(text, 80_000);

    const sloSection = options?.sloTopics?.length
      ? `Include SLO-based assessment questions for: ${options.sloTopics.join(', ')}.`
      : 'Include 3-5 SLO-based assessment questions at the end.';

    const prompt = `You are an expert teacher creating a student-friendly PowerPoint from educational material.

Subject: ${options?.subject ?? 'General'}
Grade level: ${options?.gradeLevel ?? 'Secondary/University'}
Source document: ${doc.originalName}

Create a JSON presentation plan from this document. Structure:
1. title slide
2. learning objectives slide (3-5 objectives)
3. multiple content slides breaking down key concepts
4. at least one real-life example slide
5. diagram slides where visuals would help (describe diagram in bullets)
6. summary slide
7. slo_questions slide with assessment questions

${sloSection}

Return ONLY valid JSON matching this schema:
{
  "presentationTitle": "string",
  "subtitle": "string",
  "slides": [
    { "type": "title|objectives|content|example|diagram|summary|slo_questions", "title": "string", "bullets": ["string"], "notes": "optional speaker notes" }
  ]
}

Document content:
---
${context}
---`;

    const provider = getAiProvider();
    const response = await provider.chat({
      messages: [{ role: 'user', content: prompt }],
      temperature: 0.4,
      maxTokens: 6000,
    });

    const json = this.extractJson(response.content);
    const parsed = presentationSchema.safeParse(json);
    if (!parsed.success) {
      throw new ValidationError('AI returned invalid presentation structure', parsed.error.flatten());
    }
    return parsed.data;
  }

  /** Generate plain text presentation format (copy-paste to PowerPoint) */
  async generatePlainText(plan: PresentationPlan): Promise<string> {
    let text = `=== ${plan.presentationTitle} ===\n`;
    if (plan.subtitle) {
      text += `${plan.subtitle}\n`;
    }
    text += `Generated by Quantum AI\n\n`;

    for (let i = 0; i < plan.slides.length; i++) {
      const slide = plan.slides[i];
      text += `--- SLIDE ${i + 1}: ${slide.title.toUpperCase()} ---\n`;
      text += `Title: ${slide.title}\n\n`;

      if (slide.bullets?.length) {
        text += `Content:\n`;
        slide.bullets.forEach((bullet) => {
          text += `  • ${bullet}\n`;
        });
      }

      if (slide.notes) {
        text += `\nNotes: ${slide.notes}\n`;
      }

      text += `\n`;
    }

    return text;
  }

  async generateFromDocument(
    documentId: string,
    userId: string,
    options?: { subject?: string; gradeLevel?: string; sloTopics?: string[] }
  ): Promise<{ text: string; plan: PresentationPlan; filename: string }> {
    const doc = await documentStorageService.getById(documentId, userId);
    const plan = await this.generatePlanFromDocument(documentId, userId, options);
    const text = await this.generatePlainText(plan);
    const base = doc.originalName.replace(/\.[^.]+$/, '');
    return { text, plan, filename: `${base}-presentation.txt` };
  }

  private extractJson(content: string): unknown {
    const trimmed = content.trim();
    const fenceMatch = trimmed.match(/```(?:json)?\s*([\s\S]*?)```/);
    const raw = fenceMatch ? fenceMatch[1].trim() : trimmed;
    try {
      return JSON.parse(raw);
    } catch {
      const start = raw.indexOf('{');
      const end = raw.lastIndexOf('}');
      if (start >= 0 && end > start) {
        return JSON.parse(raw.slice(start, end + 1));
      }
      throw new ValidationError('Failed to parse AI presentation JSON');
    }
  }
}

export const powerPointService = new PowerPointService();